import { getUser } from "~/utils/session.server";
import logo from "../assets/luckyartsLogo.png"

export async function loader({ request }: LoaderArgs) {
  const user = await getUser(request);
  if (!user) {
    throw redirect("/login");
  }
  return json({ user });
}

